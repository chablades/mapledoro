function areaLabel(mapId: string): string {
  return (
    AREA_LABEL_OVERRIDES[mapId] ??
    mapId
      .replace(/^map-/, "")
      .split("-")
      .map((word) => (word.length > 0 ? `${word.charAt(0).toUpperCase()}${word.slice(1)}` : word))
      .join(" ")
  );
}

